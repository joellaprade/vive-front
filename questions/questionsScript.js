var i;
var container = document.getElementById('questions')
var holdQuestions = [];

for(i = 0; i < questions.length; i++){
    holdQuestions.push(
        `<div id="${i}" class="question">
            <h3 class="question-title">${questions[i].question}<img class="arrow" src="../assets/arrow.png"></img></h3>
            <div id="question-container${i}" class="question-container">
                <p id="answer${i}" class="question-answer">${questions[i].answer}</p>
            </div>
        </div>`
    )
}
container.innerHTML = holdQuestions

container.childNodes.forEach(child => {
    child.onclick = () => {
        var question = document.getElementById(child.id);
        var answer = document.getElementById("question-container" + child.id);
        question.querySelector("h3").querySelector("img").style.transition = "transform 0.5s";
        answer.style.transition = "grid-template-rows 0.5s ease-in-out";
        answer.style.gridTemplateRows = 
            answer.style.gridTemplateRows != "1fr" ? answer.style.gridTemplateRows = "1fr" : answer.style.gridTemplateRows = "0fr";
        question.querySelector("h3").querySelector("img").style.transform =
        question.querySelector("h3").querySelector("img").style.transform != "rotate(-90deg)" ? "rotate(-90deg)" : "rotate(90deg)";
    }
})

//SET NAV IND
const setNav = (pageNum) => {
    var device = window.screen.width >= 1800 ? "d" : "m";
    if(device == "d"){
        document.getElementById(`${device + pageNum}`).innerHTML += 
        `<div style="border-radius: 100px; position: absolute; width: 40px; height: 8px; background: #F75E62; margin-top: 26px;"></div>`
    }else {
         document.getElementById(`${device + pageNum}`).style.borderBottom = "6px solid rgba(255, 255, 255, 0.9)";
    }
   
}
window.addEventListener('load', setNav)
setTimeout(() => {
    setNav(5);
    document.body.style.opacity='1'
}, 200)

